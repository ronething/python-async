a = [1, 2]
a[len(a):] = [3]  # [1, 2, 3]
a[:0] = [0]  # [0, 1, 2, 3]
a[2:2] = [4]  # [0, 1, 4, 2, 3]
a[:2] = [5, 6]  # [5, 6, 4, 2, 3]
a[2:] = [7]  # [5, 6, 7]
a[::2] = [0] * 2  # [0, 6, 0]
a[:2] = []  # [0]
print(a)

b = [1, 2, 3, 4]
del b[:2]
print(b)
